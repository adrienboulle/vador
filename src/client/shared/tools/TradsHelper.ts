export namespace TradsHelper {
  export function getTrads(lang: any): any {
    if (RAW_TRADS[lang]) {
      return RAW_TRADS[lang];
    }

    const trads = {};

    Object.keys(RAW_TRADS).forEach(key => {
      trads[key] = RAW_TRADS[key][lang];
    });

    RAW_TRADS[lang] = trads;

    return trads;
  }
}
